    void load(ArrayList<Products> list) {
        DefaultTableModel model = (DefaultTableModel) tblSP.getModel();
        model.setRowCount(0);
        String trangthai;
        for (Products products : list) {
            if (products.getTrangThai()) {
                trangthai = "Còn";
            } else {
                trangthai = "Hết";
            }
            model.addRow(new Object[]{
                products.getID(),products.getTenSP(), products.getGia(), products.getSoLuong(), products.getNgayThem(), trangthai
            });
        }
    }

