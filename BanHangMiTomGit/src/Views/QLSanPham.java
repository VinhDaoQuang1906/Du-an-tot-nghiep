    private void tblSPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSPMouseClicked
        int row = tblSP.getSelectedRow();
        Products prod = ql.getProductsList().get(row);
        txtID.setText(prod.getID());
        txtTen.setText(prod.getTenSP());
        txtAnh.setText(prod.getImg());
        txtGia.setText(Float.toString(prod.getGia()));
        txtTonKho.setText(Integer.toString(prod.getSoLuong()));
        if (prod.getTrangThai()) {
            txtTrangThai.setText("Còn");
        } else {
            txtTrangThai.setText("Hết");
        }
        labelNgayThem.setText("Ngày thêm: "+prod.getNgayThem());
        ImageIcon img = new ImageIcon(prod.getImg());
        Image original = img.getImage();
        Image resize = original.getScaledInstance(200, 175, Image.SCALE_SMOOTH);
        ImageLabel.setIcon(new ImageIcon(resize));
        ImageLabel.setBorder(BorderFactory.createLineBorder(Color.black, 5));
        
    }//GEN-LAST:event_tblSPMouseClicked

