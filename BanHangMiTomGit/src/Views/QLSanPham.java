    private void btnThemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnThemMouseClicked
        // TODO add your handling code here:
        String ten = txtTen.getText();
        String id = txtID.getText();
        Float gia = Float.parseFloat(txtGia.getText());
        Integer soluong = Integer.parseInt(txtTonKho.getText());
        Boolean trangthai;
        if(txtTrangThai.equals("Còn")){
        trangthai = true;
        }else{
        trangthai = false;
        }
        String img = txtAnh.getText();
        LocalDate currentDate = LocalDate.now();
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        String ngayThem = currentDate.format(formatter);
        Products prod = new Products(id, ten, trangthai, gia, soluong, img, ngayThem);
        Boolean kq = ql.addProduct(prod);
        if(kq){
            JOptionPane.showMessageDialog(this, "Đã thêm sản phẩm");
        }else{
            JOptionPane.showMessageDialog(this, "Xảy ra lỗi khi thêm sản phẩm");
        }
        load(ql.getProductsList());
    }//GEN-LAST:event_btnThemMouseClicked

