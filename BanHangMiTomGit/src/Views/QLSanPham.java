    private void btnDelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDelMouseClicked
        // TODO add your handling code here:
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc?","Confirmation",JOptionPane.YES_NO_OPTION);
        if(confirm == JOptionPane.YES_OPTION){
        String id = txtID.getText();
        Boolean kq = ql.delProduct(id);
        if(kq){
        JOptionPane.showMessageDialog(this, "Đã xóa sẩn phẩm");
        }else{
        JOptionPane.showMessageDialog(this, "Mã sản phẩm không tồn tại");
        }
        load(ql.getProductsList());
        }else{
        JOptionPane.showMessageDialog(this, "Đã hủy xóa");
        }
    }//GEN-LAST:event_btnDelMouseClicked

