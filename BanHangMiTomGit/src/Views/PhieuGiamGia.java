    void load(ArrayList<Coupons> list) {
        DefaultTableModel model = (DefaultTableModel) tblphieu.getModel();
        model.setRowCount(0);
        String trangthai;
        for (Coupons coupons : list) {
            if (coupons.getTrangThai() == true) {
                trangthai = "Hoạt động";
            } else {
                trangthai = "Hết hoạt động";
            }
            model.addRow(new Object[]{
                coupons.getID(), coupons.getPhanTramGiam(), coupons.getHan(), trangthai
            });
        }
    }

    void addComboNgay() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cbongay.getModel();
        for (int i = 1; i < 32; i++) {
            model.addElement(i);
        }
    }

    void addComboThang() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cbothang.getModel();
        for (int i = 1; i < 13; i++) {
            model.addElement(i);
        }
    }

    void addComboNam() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cbonam.getModel();
        LocalDate currentDate = LocalDate.now();
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        String now = currentDate.format(formatter).substring(0, 4);
        for (int i = Integer.parseInt(now); i < 2100; i++) {
            model.addElement(i);
        }
    }

