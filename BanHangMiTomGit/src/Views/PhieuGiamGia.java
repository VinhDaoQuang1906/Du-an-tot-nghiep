    private void updbtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updbtnMouseClicked
        LocalDate currentDate = LocalDate.now();
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        String now = currentDate.format(formatter);
        boolean kq = ql.updCouponStatus(now);
        if (kq) {
            JOptionPane.showMessageDialog(this, "Đã cập nhật trạng thái các phiếu hết hạn trước ngày " + now);
        } else {
            JOptionPane.showMessageDialog(this, "Không có phiếu nào quá hạn");
        }
        load(ql.getCouponList());
    }//GEN-LAST:event_updbtnMouseClicked

    private void btncoaphieuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btncoaphieuMouseClicked
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc?", "Confirmation", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            LocalDate currentDate = LocalDate.now();
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
            String now = currentDate.format(formatter);
            boolean kq = ql.delExpiredCoupon(now);
            if(kq){
                JOptionPane.showMessageDialog(this, "Đã xóa các phiếu hết hạn trước ngày "+now);
            }else{
            JOptionPane.showMessageDialog(this, "Không có phiếu nào hết hạn");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Đã hủy xóa");
        }
        load(ql.getCouponList());
    }//GEN-LAST:event_btncoaphieuMouseClicked

