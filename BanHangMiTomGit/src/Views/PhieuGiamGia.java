    private void delbtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_delbtnMouseClicked
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc?", "Confirmation", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            String id = txtdel.getText();
            boolean kq = ql.deleteCoupon(id);
            if (kq) {
                JOptionPane.showMessageDialog(this, "Đã xóa phiếu giảm giá");
            } else {
                JOptionPane.showMessageDialog(this, "Phiếu không tồn tại");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Đã hủy xóa");
        }
        load(ql.getCouponList());
    }//GEN-LAST:event_delbtnMouseClicked

