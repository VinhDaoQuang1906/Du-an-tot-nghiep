    private void taoBTNMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_taoBTNMouseClicked
        String ma = txtma.getText();
        Integer giam = Integer.parseInt(cbogiam.getSelectedItem().toString());
        String han = cbonam.getSelectedItem().toString() + "-" + cbothang.getSelectedItem().toString() + "-" + cbongay.getSelectedItem().toString();
        if(ma.trim().isBlank()){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mã phiếu giảm giá");
            return;
        }
        Coupons gg = new Coupons(ma, giam, han.trim(), true);
        boolean kq = ql.addCoupon(gg);
        if (kq) {
            JOptionPane.showMessageDialog(this, "Đã thêm phiếu giảm giá");
        } else {
            JOptionPane.showMessageDialog(this, "Xảy ra lỗi khi thêm phiếu giảm giá, vui lòng kiểm tra lại thông tin phiếu");
        }
        load(ql.getCouponList());
    }//GEN-LAST:event_taoBTNMouseClicked

