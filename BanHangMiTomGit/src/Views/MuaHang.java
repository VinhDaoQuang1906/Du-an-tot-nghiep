    private void btnCouponMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCouponMouseClicked
        // TODO add your handling code here:
        try {
            Coupons coupon = new Coupons();
            for (Coupons couponz : ql.getCouponList()) {
                if (couponz.getID().trim().equals(txtMaGiamGia.getText())) {
                    coupon = couponz;
                }
            }
            if (coupon.getTrangThai()) {
                discountUse = "yes";
                savecode = txtMaGiamGia.getText();
                saveDiscount = coupon.getPhanTramGiam();
            } else {
                JOptionPane.showMessageDialog(this, "Phiếu giảm giá này không còn hoạt động");
                return;
            }
            Float tong = Float.parseFloat(labelTong.getText().substring(6).trim());
            labelTong.setText("Tổng: " + tong * (100 - coupon.getPhanTramGiam()) / 100);
            JOptionPane.showMessageDialog(this, "Bạn đã được giảm " + coupon.getPhanTramGiam() + "%");
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Mã giảm giá không tồn tại");
        }
    }//GEN-LAST:event_btnCouponMouseClicked

    private void btnThanhToanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnThanhToanMouseClicked
        // TODO add your handling code here
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc?", "Confirmation", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            String giamGia;
            if (discountUse.equals("yes")) {
                ql.usedCoupon(savecode.trim());
                giamGia = saveDiscount + "%";
            } else {
                giamGia = "Không";
            }
            String chiTiet = "";
            int i = 0;
            for (Cart cart : cartItems) {
                if (i == 0 && cartItems.size() == 1) {
                    chiTiet = chiTiet + cart.getProduct() + " x" + cart.getAmount() + ".";
                } else if (cartItems.size() > 1 && i != cartItems.size() - 1) {
                    chiTiet = chiTiet + cart.getProduct() + " x" + cart.getAmount() + ", ";
                } else if (i == cartItems.size() - 1) {
                    chiTiet = chiTiet + cart.getProduct() + " x" + cart.getAmount() + ".";
                }
                i++;
            }
            i = 0;
            LocalDate currentDate = LocalDate.now();
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
            String ngayThanhToan = currentDate.format(formatter);
            Float tongTien = Float.parseFloat(labelTong.getText().trim().substring(6));
            String phuongThuc = cboPhuongThuc.getSelectedItem().toString();
            String trangThai = "Đang chờ";
            String ngmua = txtuser.getText();
            String diachi = txtDiaChi.getText();
            if(diachi.trim().isBlank()){
                JOptionPane.showMessageDialog(this, "Vui lòng nhập địa chỉ");
            }
            Bills bill = new Bills(chiTiet, ngayThanhToan, tongTien, trangThai, giamGia, phuongThuc, diachi, ngmua);
            Boolean kq = ql.addBill(bill);
            int j = 0;
            String delprod;
            Integer left;
            if (kq) {
                for (Cart cart : cartItems) {
                    if (cart.getProduct().trim().equals(ql.getProductsList().get(j).getTenSP())) {
                        delprod = cart.getProduct();
                        left = ql.getProductsList().get(j).getSoLuong() - cart.getAmount();
                        ql.updateProductAmount(delprod, left);
                    }
                    j++;
                }
                JOptionPane.showMessageDialog(this, "Thanh toán thành công");
            } else {
                JOptionPane.showMessageDialog(this, "Thanh toán thất bại");
            }
            saveDiscount = null;
            savecode = null;
        } else {
            JOptionPane.showMessageDialog(this, "Đã hủy thanh toán, đã khôi phục phiếu giảm giá");
        }
    }//GEN-LAST:event_btnThanhToanMouseClicked

