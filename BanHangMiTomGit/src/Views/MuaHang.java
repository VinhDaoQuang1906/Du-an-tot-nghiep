    private void addToCartButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addToCartButtonMouseClicked
        // TODO add your handling code here:
        Products prod = ql.getProductsList().get(displayProd);
        String name = prod.getTenSP();
        Integer amount = buy;
        Float total = prod.getGia() * buy;
        if (buy == 0) {
            JOptionPane.showMessageDialog(this, "Phải mua ít nhất 1 đơn vị");
            return;
        }
        Cart tempcart = new Cart();
        for (Cart cartcheck : cartItems) {
            if (name.equals(cartcheck.getProduct())) {
                amount += cartcheck.getAmount();
                total += cartcheck.getTotal();
                tempcart = cartcheck;
            }
        }

        Cart cart = new Cart(name, amount, total);
        cartItems.add(cart);
        try {
            cartItems.remove(tempcart);
        } catch (Exception e) {
            loadCart(cartItems);
        }

        loadCart(cartItems);
    }//GEN-LAST:event_addToCartButtonMouseClicked

